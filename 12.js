// booleans & comparison operator

// booleans
// true , false

let num1 = 7;
let num2 = "7";

console.log(num1 > num2);

// == vs ===
console.log(num1 == num2);    //  == checks the value is equals to or not
console.log(num1 === num2);   //  === checks the data type  is equals to or not

// != vs !==
console.log(num1 != num2);    /*  != checks the values are different or not.
                                  If values are different it print (true) otherwise it print (false) */

console.log(num1 !== num2);
                                  